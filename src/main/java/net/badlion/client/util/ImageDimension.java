package net.badlion.client.util;

public class ImageDimension
{
    private final int x;
    private final int y;

    public ImageDimension(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return this.x;
    }

    public int getY()
    {
        return this.y;
    }
}
